// Addition
function add(a,b){

    return a+b;
}

//Subtraction
function subtract(a, b) {
    return a - b;
}
//Multiplication
function multiply(a, b) {
    return a * b;
}
//Division
function divide(a, b) {
    return a / b;
}
//Increment
function increment(a) {
    return ++a ;
}
//Decrement
function decrement(b){
    return --b;
}
//Remainder
function reminder(a, b) {
    return a % b;
}


